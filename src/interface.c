#include <stdio.h>
#include <conio.h>
#include <windows.h>
#include "sprite.h"

#define Height 40
#define Width 80

void PrintScreen();
void ClearScreen();

char screen[Height][Width] = {0};

void InitScreen(){
  int i,j;
  system("cls");
  for(i = 0; i < Height; i++){
    for(j = 0; j < Width; j++){
      screen[i][j] = ' ';
    }
  } 
}

void PrintScreen(){
  int i,j;
  for(i = 0; i < Height; i++){
    for(j = 0; j < Width; j++){
      printf("%c",screen[i][j]);
    }
    printf("\n");
  }
}

void ClearScreen(){
  int i,j;

  for(i = 0; i < Height; i++){
    for(j = 0; j < Width; j++){
      screen[i][j] = ' ';
    }
  }

  PrintScreen();
}

void border(){
	int x=0, y=0;
	for(y = 0; y < Height; y++){
	    for(x = 0; x < Width; x++){
	      if(x==0 || y==0 || x==Width-1 || y==Height - 1){
	        
	        screen[y][x] = '#';
	
	      }
	    }
	  }
}

void DrawSprite(char sprite[8][16], int xl, int yl){
	int x = xl + 16 ,y = yl + 8;

	for(yl; yl<y; yl++){
	    for(xl; xl<x; xl++)
			screen[xl][yl] = sprite[xl][yl];
	}
}
